        self.strength = 1
        self.dexterity = 1
        self.constitution = 1
        self.intelligence = 1
        self.wisdom = 1
        self.charisma = 1

        self.willpower = 0
        self.spirit = 0
        self.instinct = 0
        self.presence = 0
